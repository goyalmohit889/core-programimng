//  Write a program to check if a number is divisible by 5

public class nodiv {
    public static void main(String[] args) {
        int n = 10;
        if (n % 5 == 0) {
            System.out.println("yes,divisible");
        } else {
            System.out.println("not divisible");
        }
    }
}
